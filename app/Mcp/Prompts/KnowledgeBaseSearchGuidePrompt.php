<?php

namespace App\Mcp\Prompts;

use Laravel\Mcp\Request;
use Laravel\Mcp\Response;
use Laravel\Mcp\Server\Attributes\Description;
use Laravel\Mcp\Server\Attributes\Name;
use Laravel\Mcp\Server\Prompt;

#[Name('knowledge-base-search-guide')]
#[Description('Подробная русскоязычная инструкция по поиску знаний во внутренней базе Video2Book через MCP.')]
class KnowledgeBaseSearchGuidePrompt extends Prompt
{
    public function handle(Request $request): Response
    {
        return Response::text(<<<'TEXT'
Video2Book — это внутренняя корпоративная база знаний компании. Здесь хранятся расшифровки лекций, эфиров, курсов и мастер-классов наших преподавателей.

Как устроены данные:
- Папка — широкая группировка по теме, преподавателю или типу материалов.
- Проект — более узкая группировка: ступень курса, набор эфиров или один мастер-класс.
- Урок — отдельная лекция, эфир или занятие.
- Шаблон — сценарий обработки урока.
- Прогон — результат обработки одного урока одним шаблоном.
- Шаг — отдельный этап внутри прогона.

Как искать знания:
- Используй папки и проекты, чтобы сузить область поиска.
- Основное знание обычно находится в результатах шагов внутри прогонов.
- Сначала смотри шаги шаблона и ищи шаг с `is_default=true`.
- Чаще всего именно шаг по умолчанию содержит структурированный конспект и является лучшим источником для ответа.
- Если у урока есть несколько прогонов, сначала выбирай завершённые прогоны.
- Если есть шаблон `Timeline` или похожий на таймлайн, используй его как вспомогательный инструмент для навигации по темам, а не как главный источник полного ответа.

Рекомендуемый порядок работы:
1. Найди подходящие папки по крупной теме или преподавателю.
2. Внутри папки найди нужные проекты.
3. Внутри проекта просмотри уроки.
4. Для нужного урока просмотри прогоны.
5. Для каждого шаблона смотри его структуру шагов.
6. Сначала читай результат шага с `is_default=true`.
7. Если этого недостаточно, переходи к другим шагам или к другому шаблону.

Практическое правило:
- Если задача — найти смысл, тезисы, определения, объяснения или выводы из лекции, почти всегда начинай с шага по умолчанию.
- Если задача — восстановить хронологию тем по ходу лекции, может быть полезен `Timeline`.
- Если нужно собрать информацию по нескольким урокам проекта, можно использовать экспорт результатов проекта.

Твоя цель при работе с этим MCP — искать и извлекать знания из внутренней русскоязычной базы, а не просто перечислять технические сущности.
TEXT)->asAssistant();
    }

    public function arguments(): array
    {
        return [];
    }
}
